CREATE TABLE courses (
   courseID INT AUTO_INCREMENT,
   courseNo VARCHAR(20),
   courseTitle VARCHAR(150),
   creditHrs INT,
   semester VARCHAR(30),
   PRIMARY KEY (courseID),
   INDEX idx_courseNo (courseNo),
   INDEX idx_courseTitle (courseTitle) 
   ) ENGINE=InnoDB;

